    public void actualizarPuntos(){
        for (int i = 0; i < puntosLabel.length; i++){
            if (index == i){
                utilidades.SetImageLabel(puntosLabel[i], "src/botones/PuntoCarruselFilled.png");
            }
            else {
                utilidades.SetImageLabel(puntosLabel[i], "src/botones/PuntoCarruselEmpty.png");                
            }
        }
    }
    
    public int index = 0;

    public void sumar() throws IOException {
        index++;
        if (index > 4) {
            index = 0;
        }
        utilidades.SetImageLabel(imgprincipal, "src/fotos launcher/"+utilidades.ImagenSimulador(grado,simulador)+"/" +utilidades.ImagenSimulador(grado,simulador)+ index + ".png");
        actualizarPuntos();
        
    }

    public void restar() throws IOException {
        index--;
        if (index < 0) {
            index = 4;
        }
         utilidades.SetImageLabel(imgprincipal, "src/fotos launcher/"+utilidades.ImagenSimulador(grado ,simulador)+"/" +utilidades.ImagenSimulador(grado,simulador)+ index + ".png");
        actualizarPuntos();
    }


